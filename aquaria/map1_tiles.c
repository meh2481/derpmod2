void set_aquaria_map_tile_column(uint8_t col, uint8_t vram_row, uint8_t vram_col) BANKED {
  VBK_REG = VBK_TILES;
  tmp = vram_col % 32;
  for (i = 0; i < 19; i++) {
    set_bkg_tiles(tmp, vram_row + i, 1, 1, &map1_tilesPLN0[(i + vram_row) * map1_tilesWidth + col]);
  }
}

